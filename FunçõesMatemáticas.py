# Funções matemáticas built-in e o módulo math 
# o Python possui 2 grandes tipos de funções matemáticas, o built-in (que não precisa importar nada) e o módulo math
# FUNÇÕES BUILT-IN

valores = [13,1,12,-23,-3,11]
print(max(valores)) 
# imprime o maior valor da lista de valores
print(min(valores))
# imprime o menor valor da lista de valores 
a = -5
print(abs(a))
# exibe o valor absoluto de -5 (a)
b = 8
print(abs(b))

# função de exponenciação (power)
print(pow(a,b))
# -5 elevado a 8

c = 21.234245
print(round(c,3))
